    console.log("Divison von 10 und 2:", param1 / param2);
    console.log( "Multiplikation von 30 und 20:", param3 * param4);
    console.log("Divison von 30 und 20:", param3 / param4);
    console.log( "Multiplikation von 100 und 100:", param5 * param6);
    console.log("Divison von 100 und 100:", param5 / param6);
