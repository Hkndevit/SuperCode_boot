    console.log("Divison von 5 und 0:", param7 / param8);
    console.log( "Multiplikation von 45 und 173:", param9 * param10);
    console.log("Divison von 45 und 173:", param9 / param10);
    console.log( "Multiplikation von 1 und 1000:", param11 * param12);
